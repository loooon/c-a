package com.credit.entity.dto;

import com.credit.entity.User;

/**
 * Created by Michael Chan on 4/11/2017.
 */
public class UserResourceDto extends User
{
    private String resourceName;

    private String dailyTimes;

    private String yearlyTimes;

    private String resourceId;

    private String resourcePath;

    public String getResourceId()
    {
        return resourceId;
    }

    public void setResourceId(String resourceId)
    {
        this.resourceId = resourceId;
    }

    public String getResourcePath()
    {
        return resourcePath;
    }

    public void setResourcePath(String resourcePath)
    {
        this.resourcePath = resourcePath;
    }

    public String getResourceName()
    {
        return resourceName;
    }

    public void setResourceName(String resourceName)
    {
        this.resourceName = resourceName;
    }

    public String getDailyTimes()
    {
        return dailyTimes;
    }

    public void setDailyTimes(String dailyTimes)
    {
        this.dailyTimes = dailyTimes;
    }

    public String getYearlyTimes()
    {
        return yearlyTimes;
    }

    public void setYearlyTimes(String yearlyTimes)
    {
        this.yearlyTimes = yearlyTimes;
    }
}
